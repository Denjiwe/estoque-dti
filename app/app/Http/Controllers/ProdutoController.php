    }
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $produtos = $this->produto->paginate(10);

        return view('produto.index', ['produtos' => $produtos, 'titulo' => 'Produtos Cadastrados']);
    }

